package Modelo;

import java.util.Date;
import java.util.Objects;

/**
 *
 * @author emiim
 */
public class Libro {
    private Long id;
    private String titulo;
    private String autor;
    private String isbn;
    private Date fechaLanzamiento; // Usando java.sql.Date para el ejemplo
    private String categoria;
    private double precio;
    private int cantidadStock;
    private String rutaImagen;

    public Libro() {}

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public Date getFechaLanzamiento() {
        return fechaLanzamiento;
    }

    public void setFechaLanzamiento(Date fechaLanzamiento) {
        this.fechaLanzamiento = fechaLanzamiento;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public int getCantidadStock() {
        return cantidadStock;
    }

    public void setCantidadStock(int cantidadStock) {
        this.cantidadStock = cantidadStock;
    }

    public String getRutaImagen() {
        return rutaImagen;
    }

    public void setRutaImagen(String rutaImagen) {
        this.rutaImagen = rutaImagen;
    }

    @Override
    public boolean equals(Object o) { /* Basado en id o isbn */
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Libro libro = (Libro) o;
        return Objects.equals(id, libro.id) || (id == null && Objects.equals(isbn, libro.isbn));
    }
    @Override
    public int hashCode() { return Objects.hash(id != null ? id : isbn); }
     @Override
    public String toString() { /* ... */ return "Libro{id=" + id + ", isbn='" + isbn + '\'' + '}'; }
}
