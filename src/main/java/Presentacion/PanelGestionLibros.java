    private void cargarDatosLibro() {
        if (libro != null) {
            lblTitulo.setText(libro.getTitulo());
            lblStock.setText(String.valueOf(libro.getCantidad()));
        }
        if (libro.getRutaImagen() != null && !libro.getRutaImagen().isEmpty()) {
                try {
                    ImageIcon iconOriginal = new ImageIcon(getClass().getResource(libro.getRutaImagen()));
                    if (iconOriginal.getImageLoadStatus() == java.awt.MediaTracker.ERRORED || iconOriginal.getIconWidth() == -1) {
                        lblPortada.setText("No img"); 
                        lblPortada.setIcon(null);
                        System.err.println("Error al cargar imagen (recurso no encontrado o error): " + libro.getRutaImagen());
                    } else {
                        int width = lblPortada.getWidth(); 
                        int height = lblPortada.getHeight(); 

                        
                        if (width == 0 || height == 0) {
                            Dimension prefSize = lblPortada.getPreferredSize();
                            width = (int) prefSize.getWidth();
                            height = (int) prefSize.getHeight();
                            if (width == 0 || height == 0) { 
                                width = 90; height = 115; 
                            }
                        }
                        Image img = iconOriginal.getImage().getScaledInstance(width, height, Image.SCALE_SMOOTH);
                        lblPortada.setIcon(new ImageIcon(img));
                        lblPortada.setText(""); 
                    }
                } catch (Exception e) {
                    lblPortada.setText("Error");
                    lblPortada.setIcon(null);
                    System.err.println("Excepción al cargar imagen: " + libro.getRutaImagen() + " - " + e.getMessage());
                }
            } else {
                lblPortada.setText("Sin img");
                lblPortada.setIcon(null);
            }
        }
    
    private void configurarListeners() {
        if (btnEditarLibro != null) { 
            btnEditarLibro.addActionListener(e -> {
                if (this.pantallaGestion != null && this.libro != null) {
                    System.out.println("PanelGestionLibros: Editando libro - " + this.libro.getTitulo());
                    ControlNavegacion.getInstase().navegarEditarLibro(this.pantallaGestion, this.libro);
                } else {
                    System.err.println("PanelGestionLibros: Error al intentar editar. pantallaGestion o libro es null.");
                }
            });
        } else {
            System.err.println("PanelGestionLibros: btnEditarLibro no está inicializado. Verifica el nombre en el diseñador.");
        }
    }
    
    

